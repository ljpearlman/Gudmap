package gmerg.entities;

public class ChromeDetail {

	private String chromeName;
	private String chromeStart;
	private String chromeEnd;
	
	
	public String getChromeName() {
		return chromeName;
	}
	
	public void setChromeName(String chromeName) {
		this.chromeName = chromeName;
	}

	public String getChromeStart() {
		return chromeStart;
	}
	
	public void setChromeStart(String chromeStart) {
		this.chromeStart = chromeStart;
	}

	public String getChromeEnd() {
		return chromeEnd;
	}
	
	public void setChromeEnd(String chromeEnd) {
		this.chromeEnd = chromeEnd;
	}
}
